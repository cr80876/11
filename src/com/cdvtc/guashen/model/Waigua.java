package com.cdvtc.guashen.model;

/**
 * @author 外挂信息的get set方法
 *
 */
public class Waigua {
    protected   int wgid;
    protected   String wgmc;
    protected   int wgjg;
    protected  int yxid;
    protected int glyid;
    protected  String wgbz;

    public int getWgid() {
        return wgid;
    }

    public void setWgid(int wgid) {
        this.wgid = wgid;
    }

    public String getWgmc() {
        return wgmc;
    }

    public void setWgmc(String wgmc) {
        this.wgmc = wgmc;
    }

    public int getWgjg() {
        return wgjg;
    }

    public void setWgjg(int wgjg) {
        this.wgjg = wgjg;
    }

    public int getYxid() {
        return yxid;
    }

    public void setYxid(int yxid) {
        this.yxid = yxid;
    }

    public int getGlyid() {
        return glyid;
    }

    public void setGlyid(int glyid) {
        this.glyid = glyid;
    }

    public String getWgbz() {
        return wgbz;
    }

    public void setWgbz(String wgbz) {
        this.wgbz = wgbz;
    }


}
